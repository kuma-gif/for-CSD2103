/* เขียนโปรแกรมพิมพ์ 0, 2, 4, ..., 98 */
public class a2 {
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อวนรอบตั้งแต่ 0 ถึง 98 โดยเพิ่มค่าขึ้นทีละ 2
        for (int i = 0; i <= 98; i += 2) {
            System.out.print(i); // พิมพ์ตัวเลข i ที่อยู่ในตัวแปรในแต่ละรอบของลูป
            System.out.print(" "); // พิมพ์ช่องว่างเพื่อคั่นตัวเลขให้ดูง่าย
        }
    }
}