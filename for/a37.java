public class a37 {
    public static void main(String[] args) {
        int count = 0; // ใช้เก็บจำนวนของตัวเลขที่หารด้วย 3 หรือ 5 ลงตัว

        // วนลูปเพื่อหาจำนวนทั้งหมดของตัวเลขที่หารด้วย 3 หรือ 5 ลงตัว
        for (int i = 1; i <= 10000; i++) {
            if (i % 3 == 0 || i % 5 == 0) {
                count++; // เพิ่มจำนวนที่หารด้วย 3 หรือ 5 ลงตัว
            }
        }

        // วนลูปเพื่อลดจำนวนใน count ที่เป็นจำนวนที่หารด้วย 15 ลงตัว (หารด้วย 3 และ 5
        // ทั้งคู่)
        for (int i = 1; i <= 10000; i++) {
            if (i % 15 == 0) {
                count--; // ลดจำนวนที่หารด้วย 15 ลงตัว
            }
        }

        // แสดงผลลัพธ์จำนวนของตัวเลขที่หารด้วย 3 หรือ 5 ลงตัว (ไม่รวมที่หารด้วย 15)
        // ในช่วง 1 ถึง 10000
        System.out.println(
                "จำนวนที่หารด้วย 3 หรือ 5 ลงตัวเพียงตัวใดตัวหนึ่งที่อยู่ระหว่าง 1 และ 10000 คือ: " + count + " จำนวน");
    }
}
