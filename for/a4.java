/*  เขียนโปรแกรมพิมพ์ 99, 98, 97, ..., 0 */
public class a4 {
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อวนรอบตั้งแต่ 99 ถึง 0 โดยลดค่าลงทีละ 1
        for (int i = 99; i >= 0; i--) {
            System.out.print(i); // พิมพ์ตัวเลข i ที่อยู่ในตัวแปรในแต่ละรอบของลูป
            System.out.print(" "); // พิมพ์ช่องว่างเพื่อคั่นตัวเลขให้ดูง่าย
        }
    }
}