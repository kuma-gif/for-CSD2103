/* เขียนโปรแกรมพิมพ์ 98, 96, 94, ..., 0 */
public class a5 {
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อวนรอบตั้งแต่ 98 ถึง 0 โดยลดค่าลงทีละ 2
        for (int i = 98; i >= 0; i -= 2) {
            System.out.print(i); // พิมพ์ตัวเลข i ที่อยู่ในตัวแปรในแต่ละรอบของลูป
            System.out.print(" "); // พิมพ์ช่องว่างเพื่อคั่นตัวเลขให้ดูง่าย
        }
    }
}