/*เขียนโปรแกรมพิมพ์จำนวนที่หารด้วย 3 และ 5 ลงตัว แต่หารด้วย 2 ไม่ลงตัว ซึ่งอยู่ระหว่าง 1 และ 100 */
public class a21 {
    public static void main(String[] args) {
        System.out.println("จำนวนที่หารด้วย 3 และ 5 ลงตัว แต่หารด้วย 2 ไม่ลงตัวที่อยู่ระหว่าง 1 และ 100:");

        for (int i = 1; i <= 100; i++) {
            // ตรวจสอบว่า i หารด้วย 3 และ 5 ลงตัว (i % 3 == 0 และ i % 5 == 0)
            // และหารด้วย 2 ไม่ลงตัว (i % 2 != 0)
            if (i % 3 == 0 && i % 5 == 0 && i % 2 != 0) {
                System.out.println(i);
            }
        }
    }
}
