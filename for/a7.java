/*  เขียนโปรแกรมพิมพ์ -3, -2, -1, 0, ..., 9 */
public class a7 {
    // สร้างคลาส a7 และเมธอด main เป็นเมธอดที่เริ่มต้นในการรันโปรแกรม.
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อพิมพ์ตัวเลขตามลำดับ
        for (int i = -3; i <= 9; i++) {
        System.out.print(i + " ");
        }
    }
}