/* เขียนโปรแกรมพิมพ์ 99, 97, 95, ..., 1 */
public class a6 {
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อวนรอบตั้งแต่ 99 ถึง 1 โดยลดค่าลงทีละ 2
        for (int i = 99; i >= 1; i -= 2) {
            System.out.print(i); // พิมพ์ตัวเลข i ที่อยู่ในตัวแปรในแต่ละรอบของลูป
            System.out.print(" "); // พิมพ์ช่องว่างเพื่อคั่นตัวเลขให้ดูง่าย
        }
    }
}
