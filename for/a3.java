/*  เขียนโปรแกรมพิมพ์ 1, 3, 5, ..., 99 */
public class a3 {
    public static void main(String[] args) {
        // ใช้ลูป for เพื่อวนรอบตั้งแต่ 1 ถึง 99 โดยเพิ่มค่าขึ้นทีละ 2
        for (int i = 1; i <= 99; i += 2) {
            System.out.print(i); // พิมพ์ตัวเลข i ที่อยู่ในตัวแปรในแต่ละรอบของลูป
            System.out.print(" "); // พิมพ์ช่องว่างเพื่อคั่นตัวเลขให้ดูง่าย
        }
    }
}
