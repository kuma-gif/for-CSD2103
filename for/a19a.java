/*  เขียนโปรแกรมพิมพ์จำนวนที่หารด้วย 3 หรือ 5 ลงตัวเพียงตัวใดตัวหนึ่ง ซึ่งอยู่ระหว่าง 1 และ 100 */
public class a19a {
    public static void main(String[] args) {
        // วนลูปสำหรับ i ตั้งแต่ 1 ถึง 100
        for (int i=1; i<=100; i++) {
             // เช็คเงื่อนไข: i หารด้วย 3 ลงตัวหรือ i หารด้วย 5 ลงตัว 
            if(i % 3 ==0 ^ i % 5 ==0){
                // แสดงค่าของ i ที่ผ่านเงื่อนไขด้านบน
                System.out.print(i+"");
            }
        }
    }
}
